public class ListaZeroQuestaoTreze {
    public static void main(String[] args) {
        for (int d1 = 1; d1 < 7; d1++) {
            for (int d2 = 1; d2 < 7; d2++) {
                if (d1 + d2 == 7) {
                    System.out.println(d1 + " " + d2); 
                }
            }
        }
    }
}





